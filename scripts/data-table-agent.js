#!/usr/bin/env node
'use strict';

const crypto = require('node:crypto');

const TYPE_CHECKS = {
  string: (value) => typeof value === 'string',
  number: (value) => typeof value === 'number' && Number.isFinite(value),
  integer: (value) => Number.isInteger(value),
  boolean: (value) => typeof value === 'boolean',
  iso_datetime: (value) => typeof value === 'string' && !Number.isNaN(new Date(value).getTime()),
  enum: (value, column) => Array.isArray(column.values) && column.values.includes(value),
};

function normalizeText(value) {
  if (value === undefined || value === null) return '';
  return String(value).trim();
}

function normalizeIdentifier(value, fallback = 'field') {
  const normalized = normalizeText(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '');
  return normalized || fallback;
}

function uniqueById(entries) {
  const seen = new Set();
  const result = [];
  for (const entry of entries) {
    if (!entry || seen.has(entry.id)) continue;
    seen.add(entry.id);
    result.push(entry);
  }
  return result;
}

function inferColumnType(values) {
  const present = values.filter((value) => value !== undefined && value !== null && value !== '');
  if (present.length === 0) return 'string';
  if (present.every((value) => typeof value === 'boolean')) return 'boolean';
  if (present.every((value) => Number.isInteger(value))) return 'integer';
  if (present.every((value) => typeof value === 'number' && Number.isFinite(value))) return 'number';
  if (present.every((value) => typeof value === 'string' && !Number.isNaN(new Date(value).getTime()))) {
    return 'iso_datetime';
  }
  return 'string';
}

function summarizeColumn(name, rows) {
  const values = rows.map((row) => row && row[name]);
  return {
    name,
    type: inferColumnType(values),
    nullable: values.some((value) => value === undefined || value === null || value === ''),
    sampleValues: values
      .filter((value) => value !== undefined && value !== null && value !== '')
      .slice(0, 3),
  };
}

function inspectSampleRows(rows = []) {
  const fields = new Set();
  for (const row of rows) {
    if (!row || typeof row !== 'object' || Array.isArray(row)) continue;
    for (const key of Object.keys(row)) fields.add(key);
  }
  return [...fields].sort().map((field) => summarizeColumn(field, rows));
}

function buildDataTableSchemaPlan(input = {}) {
  const useCase = normalizeText(input.useCase) || 'agentic_data_table';
  const tableName = normalizeIdentifier(input.tableName || useCase, 'agentic_data_table');
  const sampleRows = Array.isArray(input.sampleRows) ? input.sampleRows : [];
  const inferred = inspectSampleRows(sampleRows);
  const desiredMetrics = Array.isArray(input.desiredMetrics) ? input.desiredMetrics : [];
  const baseColumns = [
    {
      name: 'row_id',
      type: 'string',
      required: true,
      source: 'generated',
      description: 'Stable deterministic identifier for this generated row.',
      computation: 'sha256 hash of the normalized source row and schema version.',
    },
    {
      name: 'source_hash',
      type: 'string',
      required: true,
      source: 'generated',
      description: 'Hash of the source payload that produced the row.',
      computation: 'sha256 hash of the stable JSON source payload.',
    },
    {
      name: 'ingested_at',
      type: 'iso_datetime',
      required: true,
      source: 'generated',
      description: 'Timestamp when the row builder materialized this row.',
      computation: 'Pipeline runtime timestamp.',
    },
  ];
  const sourceColumns = inferred.map((column) => ({
    name: normalizeIdentifier(column.name),
    type: column.type,
    required: !column.nullable,
    source: 'raw',
    description: `Normalized source field: ${column.name}.`,
    computation: `Copied from source field ${column.name}.`,
    sampleValues: column.sampleValues,
  }));
  const metricColumns = desiredMetrics.map((metric) => ({
    name: normalizeIdentifier(metric),
    type: 'number',
    required: false,
    source: 'derived',
    description: `Derived metric requested by the operator: ${normalizeText(metric)}.`,
    computation: 'Computed by the row builder from source fields or left null until a deterministic transform is configured.',
  }));
  const qaColumns = [
    {
      name: 'qa_status',
      type: 'enum',
      values: ['pass', 'warn', 'fail'],
      required: true,
      source: 'qa',
      description: 'Self-review status for the generated row.',
      computation: 'Computed from schema validation and reconciliation checks.',
    },
    {
      name: 'qa_notes',
      type: 'string',
      required: false,
      source: 'qa',
      description: 'Short explanation of QA warnings or failures.',
      computation: 'Generated by the deterministic QA reviewer.',
    },
  ];
  const columns = uniqueById([...baseColumns, ...sourceColumns, ...metricColumns, ...qaColumns]
    .map((column) => ({ ...column, id: normalizeIdentifier(column.name) })));

  return {
    schemaVersion: normalizeText(input.schemaVersion) || 'schema_v1',
    tableName,
    useCase,
    owner: normalizeText(input.owner) || 'operator',
    consumers: Array.isArray(input.consumers) && input.consumers.length > 0 ? input.consumers : ['dashboard', 'agent'],
    connectors: {
      ingest: Array.isArray(input.ingestConnectors) ? input.ingestConnectors : [],
      storage: normalizeText(input.storage) || 'jsonl',
      access: Array.isArray(input.accessConnectors) ? input.accessConnectors : ['api', 'dashboard'],
    },
    columns,
    primaryKey: ['row_id'],
    approvalPolicy: {
      schemaChanges: 'human_approval_required',
      writeActions: 'always_ask',
      destructiveMigrations: 'blocked_without_rollback',
    },
  };
}

function stableStringify(value) {
  if (Array.isArray(value)) return `[${value.map((entry) => stableStringify(entry)).join(',')}]`;
  if (value && typeof value === 'object') {
    return `{${Object.keys(value).sort().map((key) => `${JSON.stringify(key)}:${stableStringify(value[key])}`).join(',')}}`;
  }
  return JSON.stringify(value);
}

function hashValue(value) {
  return crypto.createHash('sha256').update(stableStringify(value)).digest('hex');
}

function coerceValue(value, column) {
  if (value === undefined || value === null || value === '') return null;
  if (column.type === 'string') return String(value);
  if (column.type === 'number') {
    const number = Number(value);
    return Number.isFinite(number) ? number : value;
  }
  if (column.type === 'integer') {
    const number = Number(value);
    return Number.isInteger(number) ? number : value;
  }
  if (column.type === 'boolean') return value === true || value === 'true';
  if (column.type === 'iso_datetime') {
    const date = new Date(value);
    return Number.isNaN(date.getTime()) ? value : date.toISOString();
  }
  return value;
}

function buildRowsForSchema(schema, rawRows = [], options = {}) {
  const generatedAt = normalizeText(options.generatedAt) || new Date().toISOString();
  return rawRows.map((rawRow) => {
    const sourceHash = hashValue(rawRow || {});
    const row = {};
    for (const column of schema.columns || []) {
      if (column.id === 'row_id') row[column.id] = `${schema.tableName}_${sourceHash.slice(0, 16)}`;
      else if (column.id === 'source_hash') row[column.id] = sourceHash;
      else if (column.id === 'ingested_at') row[column.id] = generatedAt;
      else if (column.id === 'qa_status') row[column.id] = 'pass';
      else if (column.id === 'qa_notes') row[column.id] = '';
      else row[column.id] = coerceValue(rawRow && rawRow[column.name], column);
    }
    return row;
  });
}

function validateRow(row, schema) {
  const issues = [];
  for (const column of schema.columns || []) {
    const value = row[column.id];
    if (column.required && (value === undefined || value === null || value === '')) {
      issues.push({ column: column.id, issue: 'missing_required_value' });
      continue;
    }
    if (value === undefined || value === null || value === '') continue;
    const check = TYPE_CHECKS[column.type] || TYPE_CHECKS.string;
    if (!check(value, column)) {
      issues.push({ column: column.id, issue: `invalid_${column.type}` });
    }
  }
  return issues;
}

function reviewRowsAgainstSchema(schema, rows = [], options = {}) {
  const issues = [];
  const rowIds = new Set();
  rows.forEach((row, index) => {
    const rowIssues = validateRow(row, schema);
    if (rowIds.has(row.row_id)) rowIssues.push({ column: 'row_id', issue: 'duplicate_primary_key' });
    if (row.row_id) rowIds.add(row.row_id);
    for (const issue of rowIssues) issues.push({ rowIndex: index, ...issue });
  });
  const requiredColumns = (schema.columns || []).filter((column) => column.required).map((column) => column.id);
  const emptyRequiredColumns = requiredColumns.filter((column) => rows.every((row) => row[column] === undefined || row[column] === null || row[column] === ''));
  for (const column of emptyRequiredColumns) {
    issues.push({ rowIndex: null, column, issue: 'required_column_empty_for_all_rows' });
  }
  const status = issues.length === 0 ? 'pass' : issues.length <= Number(options.warningThreshold || 3) ? 'warn' : 'fail';
  return {
    status,
    rowCount: rows.length,
    issueCount: issues.length,
    issues,
    proposedSchemaChanges: buildSchemaEvolutionProposals(schema, rows, issues),
  };
}

function buildSchemaEvolutionProposals(schema, rows, issues) {
  const proposals = [];
  const columns = new Set((schema.columns || []).map((column) => column.id));
  const extraKeys = new Set();
  for (const row of rows) {
    for (const key of Object.keys(row || {})) {
      if (!columns.has(key)) extraKeys.add(key);
    }
  }
  for (const key of extraKeys) {
    proposals.push({
      type: 'add_column',
      column: key,
      reason: 'Rows contain a field not represented in the schema.',
      approvalRequired: true,
    });
  }
  for (const issue of issues) {
    if (issue.issue === 'missing_required_value') {
      proposals.push({
        type: 'relax_required',
        column: issue.column,
        reason: 'Generated rows contain missing values for a required column.',
        approvalRequired: true,
      });
    }
  }
  return uniqueById(proposals.map((proposal) => ({ ...proposal, id: `${proposal.type}:${proposal.column}` })));
}

function buildDataTableAgentRun(input = {}) {
  const schema = input.schema || buildDataTableSchemaPlan(input);
  const rawRows = Array.isArray(input.rawRows) ? input.rawRows : Array.isArray(input.sampleRows) ? input.sampleRows : [];
  const rows = buildRowsForSchema(schema, rawRows, input);
  const review = reviewRowsAgainstSchema(schema, rows, input);
  return {
    generatedAt: normalizeText(input.generatedAt) || new Date().toISOString(),
    agentKind: 'data_table_agent',
    schema,
    rows,
    review,
    gates: {
      schemaEvolution: schema.approvalPolicy?.schemaChanges || 'human_approval_required',
      writeActions: schema.approvalPolicy?.writeActions || 'always_ask',
      destructiveMigrations: schema.approvalPolicy?.destructiveMigrations || 'blocked_without_rollback',
    },
  };
}

module.exports = {
  buildDataTableAgentRun,
  buildDataTableSchemaPlan,
  buildRowsForSchema,
  inspectSampleRows,
  reviewRowsAgainstSchema,
};
